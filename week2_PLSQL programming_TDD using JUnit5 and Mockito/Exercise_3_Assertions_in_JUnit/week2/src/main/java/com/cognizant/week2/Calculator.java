package com.cognizant.week2;

public class Calculator {
	 public int add(int a, int b) {
	        return a + b;
	    }

	    public int divide(int a, int b) {
	        return a / b;
	    }

	    public boolean isEven(int num) {
	        return num % 2 == 0;
	    }

	    public String greet(String name) {
	        return "Hello, " + name;
	    }
}
